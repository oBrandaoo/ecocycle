                    <h7 align="justify"><p>Descartar medicamentos vencidos de forma incorreta, os consumidores contribuem com uma quantidade pequena, mas que quando acumulada causar grandes consequências. A destruição de medicamentos é um problema que ocorre em todo o mundo e é relativamente novo.
                    </p></h7>

                    <h7 align="justify"><p>Mas existe um risco ambiental inerente a este tipo de atitude devido à “micro poluentes”. E o pior é que a maioria das pessoas não sabe os danos que causam quando jogam os remédios no lixo ou no vaso sanitário.
                    </p></h7>

                    <br> </br>

                    <h5>Forma correta de descarte de medicamentos</h5><p></p>
                    
                    <h7 align="justify"><p>O descarte desses medicamentos é importante para evitar a contaminação ambiental. Caso encontre medicamentos vencidos ou em desuso, descarte-os adequadamente, separando a embalagem secundária (a caixa de papelão) da embalagem primária que contém o medicamento, que geralmente é um blister ou vidro em camadas.
                    </p></h7>

                    <br> </br>

                    <h5>Existe uma lei sobre o descarte de medicamentos?</h5><p></p>

                    <h7 align="justify"><p>A Política Nacional de Resíduos Sólidos (PNRS) torna obrigatório o descarte adequado de produtos farmacêuticos no Brasil. Em dezembro de 2020, entrou em vigor a Portaria Federal nº 10.388, que institui um sistema de logística reversa após o descarte de medicamentos de uso doméstico de uso humano – vencidos ou sem uso – e embalagens de medicamentos.
                    </p></h7>
                    <br><p>

