                    <p><h4>Como funciona:</h4></p>

<h7 align="justify"><p>As baterias são componentes eletrônicos que podem conter substâncias tóxicas, como metais pesados ​​(como chumbo, cádmio e mercúrio), que podem prejudicar o meio ambiente e a saúde humana se descartadas incorretamente. A reciclagem de  baterias é, portanto, essencial para prevenir a poluição e promover a recuperação de materiais valiosos. O processo de reciclagem de baterias geralmente inclui as seguintes etapas: 
</p></h7>

<h6>Recolha:</h6> <h7 align="justify"><p>O primeiro passo é recolher as pilhas  usadas. Isso pode ser feito através de pontos de coleta especiais, como lojas de eletrônicos, supermercados, centros de reciclagem ou pontos de coleta designados. 
</p></h7>

<h6>Segregação:</h6> <h7 align="justify"><p>Após a coleta, as células e baterias são separadas por tipo, pois diferentes tipos de baterias (por exemplo, alcalinas,  zinco-carbono,  lítio, recarregáveis, etc.) podem conter materiais diferentes  e exigir processos especiais de manuseio. alguma reciclagem. 
</p></h7>

<h6>Desmontagem:</h6> <h7 align="justify"><p>As baterias são desmontadas em pontos especiais de reciclagem. Isso requer a remoção de componentes como o revestimento externo, conectores e  circuitos eletrônicos internos. 
</p></h7>

<h6>Extração de materiais:</h6> <h7 align="justify"><p>Os componentes dentro das  baterias são então processados ​​para extrair materiais valiosos. Por exemplo, metais como  cádmio, níquel e lítio são separados e reciclados.  
</p></h7>

<h6>Neutralização:</h6> <h7 align="justify"><p>Se a bateria contiver ácidos ou substâncias corrosivas, elas podem ser neutralizadas ou tratadas adequadamente para evitar efeitos ambientais prejudiciais. 
</p></h7>

<h6>Materiais reciclados:</h6> <h7 align="justify"><p>Novos produtos são feitos de  materiais reciclados. Metais reciclados podem ser usados ​​para fabricar novas baterias, dispositivos eletrônicos e outros produtos que utilizam metais.
</p></h7>

