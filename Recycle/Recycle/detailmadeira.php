                    <p><h4>Como funciona:</h4></p>
    
<h7 align="justify"><p>A reciclagem da madeira é um processo importante para reaproveitar produtos de madeira descartados, reduzindo o desperdício e protegendo os recursos naturais. A madeira é um material valioso e versátil utilizado na construção, móveis, embalagens e muitas outras aplicações. A reciclagem de madeira envolve a coleta, classificação, processamento e reutilização de resíduos de madeira. Aqui estão as principais etapas da reciclagem de madeira:
</p></h7>

<h6>Coleta de vidros:</h6> <h7 align="justify"><p>O primeiro passo é recolher a madeira usada. Isto pode incluir madeira recuperada, paletes, móveis antigos, estruturas de madeira ou quaisquer outros produtos de madeira que já não sejam utilizados.
</p></h7>

<h6>Triagem e classificação:</h6> <h7 align="justify"><p>Depois que a madeira é coletada, ela é classificada e classificada. Eles são divididos em diferentes categorias com base na sua qualidade e potencial de reutilização. A madeira pode ser dividida em madeira para construção, madeira serrada, móveis, paletes, etc.  
</p></h7>

<h6>Remoção de contaminantes:</h6> <h7 align="justify"><p>A madeira coletada é inspecionada para remover qualquer contaminante como pregos, parafusos, tinta ou outros materiais indesejados.
</p></h7>

<h6>Trituração e moagem:</h6> <h7 align="justify"><p>A madeira é então triturada ou moída em pequenos pedaços conhecidos como “lascas” ou “serragem”. Este processo facilita o manuseio e processamento da madeira reciclada.
</p></h7>

<h6>Descontaminação:</h6> <h7 align="justify"><p>Em alguns casos, a madeira recuperada pode passar por um processo de descontaminação para remover quaisquer produtos químicos ou contaminantes indesejados. Isto é comum quando a madeira provém de paletes ou embalagens industriais.
</p></h7>

<h6>Reutilização ou produção de produtos:</h6> <h7 align="justify"><p>A madeira reciclada é utilizada para fazer novos produtos de madeira ou reutilizada para seu uso original. Isso pode incluir a produção de móveis, estruturas de madeira, pisos, revestimentos, papel reciclado e muito mais. 
</p></h7>

<h6>Pelo que pode ser trocado:</h6> <h7 align="justify"><p> Os preços dos eucaliptos em pé praticamente triplicaram desde 2019, ao mesmo tempo que a produção florestal permaneceu estagnada. O valor do metro cúbico passou de R$40,20 em 2019 para R$115,40 neste ano.
</p></h7>
