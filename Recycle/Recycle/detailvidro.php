                    <p><h4>Como funciona:</h4></p>
    
<h7 align="justify"><p>O processo de reciclagem do vidro é relativamente direto e eficiente, pois o vidro é um material altamente reciclável e pode ser reciclado infinitamente sem perder sua qualidade. 
</p></h7>

<h6>Coleta de vidros:</h6> <h7 align="justify"><p>O processo começa com a coleta dos vidros usados. Isso pode ser feito por meio de programa de coleta, lixeiras, depósitos voluntários ou  empresas especializadas em coleta de vidros. 
</p></h7>

<h6>Transporte para pontos de reciclagem:</h6> <h7 align="justify"><p>O vidro recolhido é transportado para pontos de reciclagem de vidro. Durante o transporte, é importante separar o vidro por tipo, como vidro transparente, vidro colorido ou vidro de garrafa, pois isso facilita a reciclagem.  
</p></h7>

<h6>Triagem e limpeza:</h6> <h7 align="justify"><p>No ponto de reciclagem os vidros são inspecionados e separados para retirada de possíveis impurezas como plástico, metal ou outros resíduos  misturados ao vidro. O vidro também é limpo para remover sujeira, marcas ou resíduos de alimentos.  
</p></h7>

<h6>Trituração:</h6> <h7 align="justify"><p>O vidro limpo e classificado é quebrado em pequenos pedaços chamados “cacos” ou “cristais de vidro”. Isso é feito para facilitar a fusão do vidro e economizar energia durante o processo. 
</p></h7>

<h6>Derretimento:</h6> <h7 align="justify"><p>Pedaços de vidro são aquecidos em um forno de alta temperatura a aproximadamente 1.700 graus Celsius (3.100 graus Fahrenheit). Neste estado líquido, o vidro pode ser moldado ou moldado em novos produtos.  
</p></h7>

<h6>Moldagem:</h6> <h7 align="justify"><p>O vidro fundido pode ser moldado em diferentes formatos dependendo do produto desejado. Pode ser usado para moldar garrafas, jarras, copos, embalagens, isolamento térmico ou outros vidros. 
</p></h7>

<h6> Resfriamento e têmpera:</h6> <h7 align="justify"><p>Após a moldagem, o vidro é resfriado gradualmente para endurecer. Isto pode ser feito de forma controlada para garantir a durabilidade e resistência do produto de vidro reciclado.
</p></h7>

<h6>Pelo que pode ser trocado:</h6> <h7 align="justify"><p>O vidro é um material feito de areia e outros minerais, e a areia é um material barato e amplamente utilizado no Brasil. Isto faz com que o valor dos resíduos de vidro seja baixo em comparação com outros resíduos. Por exemplo, se compararmos o vidro com o alumínio, um quilo de vidro vale 20 centavos, enquanto um quilo de alumínio vale 8 reais. Esta disparidade significa que há pouco interesse do mercado na reciclagem de vidro.
</p></h7>
