                    <p><h4>Como funciona:</h4></p>

<h7 align="justify"><p>A reciclagem de papel e papelão é um processo importante que ajuda a reduzir o desperdício, economizar recursos naturais e diminuir o impacto ambiental da produção de papel.
</p></h7>
<h6>Coleta:</h6> <h7 align="justify"> <p>O primeiro passo é recolher papel e cartão usados. Isto pode ser feito através de sistemas de recolha junto ao passeio, contentores de reciclagem públicos, sistemas de reciclagem empresariais ou escolares ou instalações especializadas em reciclagem.
</p></h7>

<h6>Separação e triagem:</h6> <h7 align="justify"><p>Após a coleta, o papel e o papelão são separados por tipo e classificados para remover impurezas como plástico, vidro ou outros materiais indesejados. Isto é muito importante para garantir a qualidade do produto reciclado.
</p>

<h6>Trituração:</h6> <h7 align="justify"><p>O papel e  papelão são  triturados em pequenos pedaços e divididos em  polpa fibrosa. Durante este processo, a tinta ou outros revestimentos são removidos. 
</p>

<h6>Limpeza e remoção de contaminantes:</h6> <h7 align="justify"><p>Limpe a pasta para remover tinta, grampos, clipes de papel e outros contaminantes. Produtos químicos e processos de flotação também são usados ​​para separar tintas e outros resíduos.  
</p>

<h6>Branqueamento (opcional):</h6> <h7 align="justify"><p>Em alguns casos, dependendo do tipo de papel e papelão produzidos, pode ser necessário um processo de branqueamento para obter a cor desejada. No entanto, muitos produtos desses produtos reciclados não passam por esta etapa.
</p>

<h6>Secagem:</h6> <h7 align="justify"><p>O papel úmido é seco com um rolo grande aquecido, tal qual o papelão também.</p>
</p>

<h6>Enrolamento e corte:</h6> <h7 align="justify"><p>O papel e o papelão são secos e enrolados em rolos maiores e depois cortados em folhas ou rolos menores dependendo do produto final.
</p>

<h6>Pelo que pode ser trocado:</h6> <h7 align="justify"><p>Em Março de 2023, o preço do papel branco caiu de R$0,64 para R$0,19 em média. Já o quilo do papelão caiu drásticamente de R$0,78 para R$0,21.
Felizmente, podemos reciclar restos de papel e papelão para diversos fins decorativos que podem ser usados em casa, basta pesquisar vídeos pela internet ensinando a fazer diversas decorações rápidas e muito bonitas.
</p>

<h7 align="justify"><p>Se não quiser fazer trabalhos manuais ou não for criativo. É melhor mandar para a reciclagem. E lembre-se: nunca jogue no lixo comum! No caso do disquete, apenas a parte magnética não será reciclada, porém o produto será enviado para incineração em fogo controlado.
</p><br></br>

