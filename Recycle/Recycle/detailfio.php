                    <p><h4>Como funciona:</h4></p>
    
<h7 align="justify"><p>A coleta de fios elétricos é uma parte importante do processo de reciclagem, contribuindo para a redução de resíduos e a recuperação de materiais valiosos. Os fios elétricos geralmente contêm metais como cobre e alumínio, que são recursos valiosos e podem ser reciclados para economizar energia e reduzir a extração de recursos naturais.
</p></h7>

<h6>Identificação e classificação:</h6> <h7 align="justify"><p>O material e a espessura do fio podem variar. Antes da coleta é importante identificar e classificar os fios de acordo com sua composição. Por exemplo, o fio de cobre é ideal para reciclagem devido ao valor do metal.
</p></h7>

<h6>Desmontagem e desconexão:</h6> <h7 align="justify"><p>O equipamento ou sistema elétrico que contém os fios é normalmente desmontado e desconectado antes da coleta dos fios. Isso facilita o manuseio e o processamento posterior.
</p></h7>

<h6>Coleta seletiva:</h6> <h7 align="justify"><p>A coleta de cabos pode ocorrer em diferentes ambientes, como em empresas de reciclagem, instalações de reciclagem de metais ou programas especiais de coleta de resíduos. É importante ser seletivo na hora de coletar e separar os fios elétricos de outros tipos de resíduos.
</p></h7>

<h6>Separação por tipo de metal:</h6> <h7 align="justify"><p>Após a coleta, os fios são normalmente transportados para instalações especializadas de reciclagem. Lá, eles classificam os diferentes tipos de metais presentes nos fios, como cobre, alumínio, etc., por meio de um processo de separação.
</p></h7>

<h6>Tratamento e processamento:</h6> <h7 align="justify"><p>Os fios são então tratados para remover isolamento e revestimentos. Isto pode ser feito por trituração, corte ou outros métodos, dependendo do tipo de fio e do processo específico de reciclagem utilizado.
</p></h7>

<h6>Fusão e refino:</h6> <h7 align="justify"><p>Os materiais provenientes do processamento do fio são frequentemente derretidos para recuperar o metal. Por exemplo, o cobre pode ser fundido para criar lingotes de cobre puro.
</p></h7>

<h6>Comercialização e uso final:</h6> <h7 align="justify"><p>O metal recuperado do fio reciclado é então vendido para indústrias que o utilizam na fabricação de novos produtos. Por exemplo, o cobre reciclado pode ser usado para produzir fios, tubos, componentes eletrônicos e outros produtos.
</p></h7>

<h6>Pelo que pode ser trocado:</h6> <h7 align="justify"><p>A prática de troca dos fios elétricos não é muito comum, mas se analisar o preço dos seus materiais, como o cobre e o alumínio, percebe-se que: o quilo do cobre varia entre R$25,00 até R$38,00, já o preço do alumínio varia entre R$5,00 até R$6,50.
</p></h7>
