                    <p><h4>Como funciona:</h4></p>
    
<h7 align="justify"><p>A coleta de lixo orgânico é uma prática essencial para gerenciar resíduos de forma eficiente, reduzir a quantidade de resíduos enviados para aterros sanitários e promover a sustentabilidade ambiental. Os resíduos orgânicos incluem materiais biodegradáveis, como restos de alimentos, cascas de frutas, legumes, folhas, aparas de grama e outros materiais compostáveis.
</p></h7>

<h6>Separação na fonte:</h6> <h7 align="justify"><p>A coleta de resíduos orgânicos geralmente começa no ponto de origem, ou seja. residências, empresas e instituições. As pessoas são incentivadas a separar os resíduos e colocar os materiais orgânicos em recipientes especiais.
</p></h7>

<h6>Coleta diferenciada:</h6> <h7 align="justify"><p>Muitos locais possuem sistemas de coleta diferenciados que incluem dias específicos de coleta de resíduos orgânicos. Os resíduos orgânicos são coletados separadamente de outros resíduos, como papel, plástico e metal.
</p></h7>

<h6>Contêineres específicos:</h6> <h7 align="justify"><p>Recipientes especiais são reservados para resíduos orgânicos para facilitar a coleta. Esses recipientes costumam ser lacrados para evitar odores desagradáveis ​​e manter os animais afastados.
</p></h7>

<h6>Transporte:</h6> <h7 align="justify"><p>Após a coleta, os resíduos orgânicos são transportados para áreas especiais de processamento. Isto pode incluir instalações de compostagem, biodigestores ou outros métodos de processamento orgânico.
</p></h7>

<h6>Compostagem:</h6> <h7 align="justify"><p>A compostagem é um método comum de processamento de resíduos orgânicos. Nesse processo, os microrganismos decompõem os materiais orgânicos para criar o composto orgânico, um fertilizante natural que pode ser usado para melhorar a qualidade do solo.
</p></h7>

<h6>Produção de energia:</h6> <h7 align="justify"><p>O biogás produzido por biodegradação pode ser utilizado como fonte de energia que promove a produção de eletricidade ou gás para uso local.
</p></h7>

<h6>Fertilizantes naturais:</h6> <h7 align="justify"><p>O composto orgânico produzido pela compostagem pode ser utilizado como fertilizante natural rico em nutrientes que promove a saúde do solo e reduz a necessidade de fertilizantes químicos.
</p></h7>

<h6>Pelo que pode ser trocado:</h6> <h7 align="justify"><p>Os resíduos orgânicos, constituídos principalmente por resíduos biodegradáveis, como restos de alimentos, cascas de frutas e vegetais, folhas, são frequentemente destinados à compostagem ou biodegradação para produzir composto orgânico ou biogás. A troca direta de resíduos orgânicos por bens ou serviços não é uma prática comum, pois esses resíduos costumam passar por processos especiais de tratamento.
</p></h7>
