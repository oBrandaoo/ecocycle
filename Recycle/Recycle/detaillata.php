                    <p><h4>Como funciona:</h4></p>
<h7 align="justify"><p>O processo começa com a coleta das latas de alumínio usadas. Isto pode ser conseguido através de contentores públicos de reciclagem, sistemas de recolha seletiva, pontos de entrega voluntária ou programas de reciclagem organizados por empresas e organizações.
</p></h7>

<h6>Transporte para instalações de reciclagem:</h6> <h7 align="justify"><p>Após a coleta, as latas de alumínio são transportadas para instalações de reciclagem. Isso pode ser feito por caminhões coletores ou empresas de reciclagem que coletam materiais recicláveis.
</p></h7>

<h6>Triagem:</h6> <h7 align="justify"><p>No depósito de reciclagem, as latas de alumínio são descarregadas e passam por um processo de triagem para retirada de materiais indesejados, como papel, plástico ou entulhos. As latas também são separadas de outros tipos de metal.
</p></h7>

<h6>Trituração e Limpeza:</h6> <h7 align="justify"><p>Após a triagem, as latas de alumínio são trituradas em pequenos pedaços e limpas para remover impurezas como tintas, vernizes e revestimentos. Isto é muito importante para garantir a qualidade do alumínio reciclado.
</p></h7>

<h6>Fabricação de novas latas:</h6> <h7 align="justify"><p>Folhas de alumínio laminado são cortadas e moldadas para formar a lata. As latas são então estampadas com o design apropriado e revestidas internamente para proteger o conteúdo da corrosão.
</p></h7>

<h6>Distribuição:</h6> <h7 align="justify"><p>As novas latas são distribuídas para a indústria de bebidas e alimentos, onde são envasadas e lacradas.
</p></h7>

<h6>Consumidor:</h6> <h7 align="justify"><p>As latas recicladas são utilizadas para embalar uma variedade de produtos, incluindo bebidas, alimentos enlatados e produtos químicos. Os consumidores compram esses produtos em latas de alumínio recicladas.
</p></h7>

<h6>Pelo o que pode ser trocado:</h6> <h7 align="justify"><p>Como já mencionada, a venda e compra da sucata de latinha é realizada por empresas especializadas. Por isso, no momento de procurar pelos serviços é interessante procurar por uma delas. É recomendado dar prioridade para empresas especializados que possuem ISO 9001 e certificações do IBAMA e CETESB.
O quilo da latinha de alumínio vale de R$2,40 a R$3,70, com uma variação de 54,17%.</p></h7>

