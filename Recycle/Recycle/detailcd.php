                    <p><h4>Como funciona:</h4></p>
<h6>Desmontagem:</h6> Para reciclar CDs e DVDs, eles geralmente precisam ser destruídos. Isto envolve separar a camada de plástico transparente (poliestireno) da camada de alumínio que contém os dados. Plásticos e metais são separados e reciclados.
<p>
<h6>Reciclagem de Plático:</h6> O plástico dos CDs e DVDs é frequentemente reciclado para fabricar novos produtos plásticos. Ele pode ser derretido e transformado em pellets de plástico, que podem ser usados ​​para fazer estojos de CD / DVD, caixas vazias de CD / DVD e outros produtos plásticos.
</p>
<p>
<h6>Processamento de aluminio:</h6> A camada de alumínio dos CDs e DVDs pode ser reciclada para produzir um novo aluminio. Este processo de reciclagem preserva os recursos naturais, reduz a mineração e economiza energia em comparação com a produção de alumínio a partir de matérias-primas virgens.
</p>
<p>
<h6>Pelo o que pode ser trocado:</h6>A melhor opção é reaproveitar os materiais. CDs e DVDs podem se transformar em peças ornamentais para decorar sua casa. Você pode, por exemplo, pintá-los ou encapá-los para fazer porta copos.
</p>

Se não quiser fazer trabalhos manuais ou não for criativo. É melhor mandar para a reciclagem. E lembre-se: nunca jogue no lixo comum! No caso do disquete, apenas a parte magnética não será reciclada, porém o produto será enviado para incineração em fogo controlado.

