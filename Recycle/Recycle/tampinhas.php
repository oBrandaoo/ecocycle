            <h1 class="display-4 text-uppercase text-center mb-5">A Reciclagem das Tampinhas plásticas</h1>
            <div class="row justify-content-center">
                    <img class="w-75 mb-4" src="img/tampinhas2.jpg" alt="">

                <div class="col-lg-11 mb-4">
                    <br> </br>

                    <h7 align="justify"><p> O mercado de reciclagem de tampas de garrafas plásticas faz parte do esforço para construir uma economia ambientalmente sustentável. Torna-se notório quando consideramos que aproximadamente 40% das embalagens produzidas no país são de plástico.
                    </p></h7>

                    <h7 align="justify"><p> Isto significa que, embora os resíduos plásticos tenham um enorme potencial para prejudicar a saúde do planeta, envenenando o solo, os rios e os oceanos durante centenas de anos, representam uma oportunidade de negócio significativa.
                    </p></h7>

                    <h7 align="justify"><p> A indústria da reciclagem é capaz de absorver grandes quantidades de resíduos e transformá-los em matéria-prima para a produção de embalagens e diversos outros tipos de mercadorias. As tampas de garrafas plásticas coloridas podem ser separadas por cor e transformadas em novas tampas de garrafas ou outros produtos.
                    </p></h7>

                    <br> </br>

                    <h5>Benefícios da reciclagem de tampas plásticas</h5><p></p>
                    
                    <h7 align="justify"><p>Os benefícios da indústria da reciclagem para a sociedade são enormes e consideráveis ​​antes de começarmos a descartar incorretamente os nossos resíduos sólidos. Ao reciclar tampas plásticas, ajudamos:
                    </p></h7>

                <ul>
                    <h7 align="justify"><p><li>Reduzir a dependência de matérias-primas não renováveis ​​(petróleo);</li>
                    </p></h7>
                    <h7 align="justify"><p><li>Reduz a poluição proveniente de resíduos plásticos, que podem ser reciclados diversas vezes antes de serem descartados;</li>
                    </p></h7>
                    <h7 align="justify"><p><li>Gerar atividades económicas circulares, como cooperativas de recolha de resíduos, que criam empregos e rendimentos para milhares de trabalhadores vulneráveis.</li>
                    </p></h7>
                </ul>

                    <br> </br>

                    <h5>Onde descartar tampinhas plásticas em São Paulo</h5><p>
                    <br>

                    <a href="https://tampinhalegal.com.br/web/"><h6>Tampinha Legal</h6></a>
                    <h7 align="justify"><p>Com mais de 408 milhões de tampinhas recolhidas nos últimos cinco anos, é considerado o maior programa de educação socioambiental em economia circular iniciado pela indústria de transformação de plásticos na América Latina.
                    </p></h7>
                    <h7 align="justify"><p>As organizações assistenciais cadastradas no Tampinha Legal recebem todos os seus recursos financeiros provenientes da venda de resíduos plásticos reciclados para a indústria.
                    </p></h7>
                    <h7 align="justify"><p>Desde 2016, o projeto arrecadou mais de R$ 1,5 milhão, que foram distribuídos para mais de 340 instituições de caridade em todo o Brasil.
                    </p></h7>   
                    <br></br><p>

                    <a href="https://tampinhasquecuram.com.br"><h6>Tampinhas que curam</h6></a>
                    <h7 align="justify"><p>Tampinhas Que Curam é um projeto socioambiental que coleta tampinhas plásticas, vende os resíduos para empresas de reciclagem e destina recursos para ajudar crianças com câncer.
                    </p></h7>
                    <h7 align="justify"><p>O programa aceita qualquer tipo de tampas plásticas em 16 pontos de coleta na cidade de São Paulo.
                    </p></h7>
                    <h7 align="justify"><p>São aceitas tampinhas de água, suco, leite, refrigerante, queijo, maionese, achocolatado, margarina, sorvete e cápsulas de café.
                    </p></h7>  
                    <br></br><p>

                    <a href="https://www.ecopatas.org.br/donations.html"><h6>EcoPatas</h6></a>
                    <h7 align="justify"><p>Essa é a missão da ONG Ecopatas, que desde 2019 vem realizando um trabalho realmente incrível em São Paulo.
                    </p></h7>
                    <h7 align="justify"><p>Eles coletam lacres e tampas de latas plásticas, vendem os resíduos para empresas de reciclagem e usam o dinheiro para castração dos animais.
                    </p></h7>
                    <h7 align="justify"><p>A ONG afirma ter castrado mais de cinco mil animais desde o início da campanha, entre cães, gatos e até coelhos.
                    </p></h7>
                    <br></br>

