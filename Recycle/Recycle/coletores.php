            <h1 class="display-4 text-uppercase text-center mb-5">Os catadores de reciclagem</h1>
            <div class="row justify-content-center">
                    <img class="w-75 mb-4" src="img/catador.jpg" alt="">

                <div class="col-lg-11 mb-4">
                    <br> </br>
                <h5><p>O papel deles na reciclagem</p></h5>
                    <h7 align="justify"><p>Quando falamos em catadores, estamos nos referindo àqueles que cuidam do meio social. 
                    Desempenham um papel importante na gestão de resíduos sólidos, envolvendo principalmente a 
                    coleta seletiva, triagem, processamento e comercialização desses materiais recicláveis.</p></h7>

                    <h7 align="justify"><p>Dados de pesquisa do Instituto de Pesquisa Econômica Aplicada (IPEA) estimam o número desses 
                    trabalhadores no Brasil entre 400 e 600 mil. Cerca de 20 mil deles estão no estado de São Paulo, 
                    a cidade mais populosa do país, que produz em média 20 mil toneladas de lixo por dia, ou um quilo 
                    e seiscentos gramas por pessoa. Esse percentual fica ainda mais impressionante quando se considera 
                    que apenas 1% de todos esses resíduos são reciclados. Além disso, 90% do árduo e valioso processo 
                    é realizado por catadores de materiais recicláveis, que honestamente utilizam seu trabalho para 
                    prestar serviços ambientais à nossa sociedade.</p><br></br></h7>

                <h5><p>Identidade dos trabalhadores</p></h5>
                    <h7 align="justify"><p>Invisíveis para a sociedade e diretamente envolvidos no processo de reciclagem, os catadores
                    têm lutado por reconhecimento e direitos na sociedade brasileira. O “lixo” tem um valor 
                    enorme para quem vive dele.</p></h7>

                    <h7 align="justify"><p>Do nascer ao pôr do sol, eles vasculham as ruas em busca de latas e lixões contendo 
                    materiais que possam ser usados, reaproveitados ou reciclados industrialmente. Além disso,
                    coletam, separam, agregam e transportam, transformando resíduos antes considerados descartáveis
                    em mercadorias com valor de uso e troca. Porém, o trabalho é cansativo e, por ser realizado na 
                    rua, expõe os catadores a diversos riscos à saúde decorrentes da exposição a materiais contaminados
                    e ao peso de puxar carroças.</p></h7>

                    <h7 align="justify"><p>Segundo o Movimento Nacional de Catadores de Materiais Recicláveis (MNCR), existem 800 mil desses
                    profissionais em atividade no país, dos quais cerca de 85 mil estão associados ao movimento nacional.</p><br></br></h7>

                <h5><p>Impactos</p></h5>
                    <h7 align="justify"><p>As altas taxas de reciclagem do país são mantidas graças ao esforço dos catadores. Ao coletar
                    resíduos e vendê-los para reciclagem, prestam um serviço utilitário porque reduzem a quantidade de
                    materiais que, se descartados, ocupariam aterros e lixões, aumentando assim os volumes de resíduos
                    e diminuindo a longevidade desses espaços. para jogar fora. Esses trabalhadores são responsáveis pela
                    coleta, separação e transporte dos resíduos sólidos, que antes eram considerados inúteis e hoje são
                    considerados mercadoria.</p></h7>

                    <h7 align="justify"><p>“Se há reciclagem em São Paulo, é trabalho dos catadores”, disse Elisabeth Grinberg, coordenadora 
                    executiva do Instituto Polis.</p></h7>

                    <h7 align="justify"><p>O papel destes coletores e transportadores na reciclagem é importante não apenas na cidade, mas em
                    todo o país. Eles são responsáveis ​​por quase metade da coleta seletiva do Brasil, segundo estudo 
                    divulgado no mês passado pelo Instituto de Energia e Meio Ambiente da Universidade de São Paulo (USP).</p><br></br></h7>

                <h5><p>Um aplicativo</p></h5>
                    <h7 align="justify"><p>Além dessas ações, a Pimp lançou em julho de 2016 um aplicativo, o CATAKI, desenvolvido para trazer
                    uma perspectiva consciente e conectar catadores (mais de 400 cadastrados) com recicladores para que 
                    eles possam organizar os recicladores entre si. materiais. Tudo isso pode ou não ser concedido ao 
                    trabalhador, mas é recomendável que você pague pelo serviço conforme for por sua conta.</p></h7>
