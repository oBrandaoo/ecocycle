                    <p><h4>Como funciona:</h4></p>
    
<h7 align="justify"><p>A coleta de resíduos radioativos é uma atividade estritamente regulamentada e controlada devido aos riscos associados à radiação. Os resíduos radioativos podem ser gerados a partir de uma variedade de atividades, tais como produção de energia nuclear, medicina nuclear, pesquisa científica e outras aplicações industriais. A coleta, tratamento e destinação desses resíduos requerem planejamento e execução cuidadosos para garantir a segurança pública e a proteção ambiental. 
</p></h7>

<h6>Classificação dos resíduos:</h6> <h7 align="justify"><p>Os resíduos radioativos são divididos em diferentes categorias com base na sua fonte, tipo e nível de radiação. Esta classificação determina procedimentos adequados de coleta e manuseio.
</p></h7>

<h6>Embalagem segura:</h6> <h7 align="justify"><p>Os resíduos radioativos são embalados em recipientes resistentes projetados para evitar vazamentos ou danos durante o transporte. As embalagens devem atender a rígidos padrões de segurança e ser adequadas para tipos específicos de resíduos.
</p></h7>

<h6>Identificação e rotulagem:</h6> <h7 align="justify"><p>Todos os contentores de resíduos radioativos devem ser devidamente identificados e rotulados para indicar o tipo de resíduo, a sua origem e qualquer informação relevante sobre os níveis de radiação.
</p></h7>

<h6>Treinamento e qualificação:</h6> <h7 align="justify"><p>O pessoal envolvido na recolha de resíduos radioactivos deve receber formação especializada em segurança radiológica e gestão de resíduos. Isto inclui o manuseio adequado de materiais, o uso de equipamentos de proteção individual (EPI) e a compreensão dos regulamentos aplicáveis.
</p></h7>

<h6>Transporte seguro:</h6> <h7 align="justify"><p>O transporte de resíduos radioativos deve seguir normas específicas de segurança. Isto pode envolver a utilização de veículos especializados, rotas pré-determinadas e estrito cumprimento de regulamentações nacionais e internacionais.
</p></h7>

<h6>Armazenamento temporário:</h6> <h7 align="justify"><p>Em alguns casos, os resíduos radioativos podem ser armazenados temporariamente em instalações designadas antes do tratamento ou eliminação final. Essas instalações devem atender a padrões rígidos de segurança.
</p></h7>

<h6>Tratamento:</h6> <h7 align="justify"><p>Dependendo do tipo de resíduo radioativo, podem ser necessários processos de tratamento específicos, como solidificação, incineração ou outros métodos aprovados, para reduzir o volume e o perigo dos resíduos.
</p></h7>

<h6>Pelo que pode ser trocado:</h6> <h7 align="justify"><p>A troca de lixo radioativo por bens ou serviços não é uma prática comum nem recomendada devido à natureza perigosa e altamente regulamentada desses resíduos. O lixo radioativo é gerado em atividades que envolvem materiais radioativos, como usinas nucleares, hospitais que utilizam medicina nuclear e instalações de pesquisa científica. Devido aos riscos à saúde humana e ao meio ambiente associados à radiação, a gestão do lixo radioativo é uma responsabilidade séria e é regulamentada por agências governamentais em todo o mundo.
</p></h7>
