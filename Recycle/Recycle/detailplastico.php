                    <p><h4>Como funciona:</h4></p>

<h7 align="justify"><p>O plástico é um material amplamente utilizado devido à sua versatilidade, durabilidade e leveza, mas sua disposição inadequada pode causar poluição ambiental significativa. A reciclagem de plásticos ajuda a reduzir a demanda por matéria-prima virgem, economiza energia e recursos naturais e diminui a quantidade de plásticos que acabam em aterros sanitários ou no meio ambiente.
</p></h7>

<h6>Coleta:</h6><h7 align="justify"><p>O primeiro passo é coletar o plástico descartado. Isto pode ser feito através de sistemas de recolha junto ao passeio, contentores de reciclagem públicos, instalações de reciclagem ou centros de recolha designados. 
</p></h7>

<h6>Classificação:</h6> <h7 align="justify"><p>Após a coleta, os materiais plásticos são separados e classificados por tipo. O plástico possui diferentes propriedades físicas e químicas, e essa classificação é importante para garantir a qualidade do material reciclado. 
</p></h7>

<h6>Limpeza:</h6> <h7 align="justify"><p>Lave o plástico coletado para remover sujeira, resíduos de alimentos e contaminantes. Moagem: O plástico puro é moído em pequenos pedaços chamados “chips” ou “pellets”. A trituração torna mais fácil o derretimento e a formação do plástico. 
</p></h7>

<h6>Derretimento e fundição:</h6> <h7 align="justify"><p>As folhas de plástico são derretidas e moldadas em pellets ou pellets que podem ser usados ​​para fazer produtos plásticos. Este é um processo vital de reciclagem de plástico  para criar novos produtos. 
</p></h7>

<h6>Fabricação de produtos:</h6> <h7 align="justify"><p>Partículas de plástico reciclado são utilizadas na fabricação de diversos produtos, como embalagens, potes, garrafas, brinquedos, móveis, roupas, etc. Testes e certificação de qualidade: Em alguns casos, os produtos plásticos reciclados podem passar por testes e certificação de qualidade  para garantir que atendam aos padrões especificados de segurança e desempenho.
</p></h7>

<h6>Pelo o que pode ser trocado:</h6><h7 align="justify"><p>O quilo da garrafa pet vazia custa em média R$1,69. Já o quilo da tampinha a indústria da reciclagem está pagando cerca de R$2,50.</p>
</p></h7>

