            <h1 class="display-4 text-uppercase text-center mb-5">A Reciclagem dos Lacres</h1>
            <div class="row justify-content-center">
                    <img class="w-75 mb-4" src="img/lacre2.jpg" alt="lacres">

                    <div class="col-lg-11 mb-4">
                    <br> </br>

                    <h7 align="justify"><p>A reciclagem de lacres desempenha um papel significativo na promoção da sustentabilidade ambiental. Em primeiro lugar, a reciclagem desses pequenos itens, como os lacres de latas, contribui para a redução do lixo plástico no meio ambiente.
                    </p></h7>

                    <h7 align="justify"><p>Além disso, a reciclagem de lacres não apenas beneficia o meio ambiente, mas também tem um impacto social positivo. Muitas organizações ao redor do mundo coletam lacres como parte de programas de arrecadação de fundos para auxiliar em causas humanitárias, especialmente para a compra de cadeiras de rodas. 
                    </p></h7>

                    <br> </br>

                    <h5>Como os Lacres viram cadeiras de rodas?</h5><p></p>
                    
                    <h7 align="justify"><p>Os lacres não são derretidos e se transformam em cadeiras de rodas. Esse é um mito muito antigo. Eles são vendidos para empresas especializadas em reciclagem de alumínio. O valor é revertido nas cadeiras.
                    </p></h7>
                
                    <h7 align="justify"><p>Portanto, separar o lacre das latas depende diretamente da finalidade a que se deseja destinar. Existem três opções:
                    </p></h7>
                <ol>    
                    <h7 align="justify"><p><li> A primeira é retirar o lacre e encaminhá-lo para alguma iniciativa social que troque esses resíduos por cadeiras de rodas e faça doações;</li>
                    </p></h7>
                    
                    <h7 align="justify"><p><li>Outra opção é jogá-lo no lixo comum. Neste caso, não é necessário retirar o lacre do frasco. O lacre é composto por uma pequena quantidade de alumínio e seu valor no mercado de reciclagem é maior quando é fixado na lata;</li>
                    </p></h7>

                    <h7 align="justify"><p><li>O terceiro é um presente para um artesão especializado no desenvolvimento de produtos feitos a partir de lacres.</li>
                    </p></h7>
                </ol>
                    <br> </br>
                    <p>

                    <h5>Projeto Lacrando e Reciclando</h5><p></a>

                    <h7 align="justify"><p><a href="https://www.instagram.com/projetolacrandoereciclando/?utm_source=ig_web_button_share_sheet&igshid=OGQ5ZDc2ODk2ZA==">Lacrando e Reciclando</a> é uma iniciativa que visa mobilizar a comunidade e dar significado socioambiental aos lacres de latas de alumínio. O objetivo é fazer a diferença na vida de pessoas vulneráveis ​​que precisam de cadeiras de rodas.
                    </p></h7>

                    <h7 align="justify"><p>A equipe do projeto estima que sejam necessárias 140 garrafas de dois litros cheias de embalagens para cada aparelho doado.
                    </p></h7>

                    <h7 align="justify"><p>Em operação desde maio passado, a Lacrando e Reciclando conta com pontos de coleta nas regiões da Barra Funda e do Jaraguá.
